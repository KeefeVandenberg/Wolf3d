#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned	i;
	char		*new;

	i = 0;
	if (!(s))
		return (NULL);
	if (!(new = (char*)malloc(sizeof(char) * ft_strlen(s) + 1)))
		return (NULL);
	if (!new)
		return (NULL);
	while (s[i])
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
