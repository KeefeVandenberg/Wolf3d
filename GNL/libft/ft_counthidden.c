#include "libft.h"

int	ft_counthidden(char *s)
{
	int i;
	int hidden;

	i = 0;
	hidden = 0;
	while (s[i])
	{
		if (s[i] == ' ' || s[i] == '\t' || s[i] == '\n' || s[i] == '\v'
			|| s[i] == '\f' || s[i] == '\r')
			hidden++;
		i++;
	}
	return (hidden);
}
